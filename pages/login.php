		e desde então se espalhou pelo mundo. Hoje, o RPG está
		em todos os lugares, em todas as mídias — videogames,
		séries de TV e streams, como as realizadas pelo Cellbit.
		<br><br>
		No RPG, um grupo de amigos se reúne para criar uma
		história em conjunto. Um dos participantes será o mestre.
		Ele começa a narrativa, estabelecendo um cenário e uma
		situação.
